#!/usr/bin/env node
// Dump connection tokens from the dev DB to stdout, formatted for .env.test.
//
// Usage:
//   node scripts/dump-test-connection.mjs <planId>
//   node scripts/dump-test-connection.mjs <planId> >> .env.test
//
// Requires that the plan has a CONNECTED source (Salesforce) and/or
// destination (HubSpot). Run after completing the OAuth round-trip in the UI.

import { PrismaClient } from '@prisma/client'

const planId = process.argv[2]
if (!planId) {
  console.error('Usage: node scripts/dump-test-connection.mjs <planId>')
  process.exit(1)
}

const prisma = new PrismaClient()

try {
  const plan = await prisma.migrationPlan.findUnique({
    where: { id: planId },
    include: { sourceConnection: true, destinationConnection: true },
  })
  if (!plan) {
    console.error(`Plan ${planId} not found.`)
    process.exit(2)
  }

  const lines = [
    '# Generated by scripts/dump-test-connection.mjs — paste into .env.test',
    `# Source plan: ${plan.id} (${plan.name})`,
    '',
  ]

  if (plan.sourceConnection && plan.sourceConnection.adapterType === 'salesforce') {
    const cfg = JSON.parse(plan.sourceConnection.config)
    lines.push('# --- Salesforce (source) ---')
    lines.push(`SF_TEST_ACCESS_TOKEN=${cfg.accessToken}`)
    lines.push(`SF_TEST_INSTANCE_URL=${cfg.instanceUrl}`)
    if (cfg.refreshToken) lines.push(`SF_TEST_REFRESH_TOKEN=${cfg.refreshToken}`)
    if (cfg.orgName) lines.push(`SF_TEST_ORG_NAME=${cfg.orgName}`)
    lines.push('')
  } else {
    lines.push('# (No Salesforce source connection on this plan)')
    lines.push('')
  }

  if (plan.destinationConnection && plan.destinationConnection.adapterType === 'hubspot') {
    const cfg = JSON.parse(plan.destinationConnection.config)
    lines.push('# --- HubSpot (destination) ---')
    lines.push(`HS_TEST_ACCESS_TOKEN=${cfg.accessToken}`)
    if (cfg.refreshToken) lines.push(`HS_TEST_REFRESH_TOKEN=${cfg.refreshToken}`)
    if (cfg.portalId) lines.push(`HS_TEST_PORTAL_ID=${cfg.portalId}`)
    if (cfg.portalName) lines.push(`HS_TEST_PORTAL_NAME=${cfg.portalName}`)
    lines.push('')
  } else {
    lines.push('# (No HubSpot destination connection on this plan)')
    lines.push('')
  }

  console.log(lines.join('\n'))
} finally {
  await prisma.$disconnect()
}
